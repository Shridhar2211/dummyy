#!   /bin/bash  -x


read -p "enter the number;" day




if [ $day -eq 1 ]
then
    echo " monday "



elif [ $day -eq 2 ]
then
    echo " tuesday "



elif [ $day -eq 3 ]
then
    echo " wednsday "


elif [ $day -eq 4 ]
then
    echo " thrusday "


elif [ $day -eq 5 ]
then
    echo " friday "


elif [ $day -eq 6 ]
then
    echo " saturday "


elif [ $day -eq 7 ]
then
    echo " sunday "
fi


